def compute_inventory_cap(
    legacy_max: float | None,
    ref: float | None,
    above: float | None,
) -> tuple[float | None, dict[str, Any]]:
    """解析有效 base 上沿与元数据（供日志与快照）。"""
    meta: dict[str, Any] = {
        "mode": "none",
        "ref": ref,
        "above": above,
        "legacy_max": legacy_max,
        "effective_max": None,
    }
    if ref is not None and ref > 0 and above is not None and above >= 0:
        m = ref + above
        meta["mode"] = "range"
        meta["effective_max"] = m
        return m, meta
    if ref is not None and ref > 0:
        meta["mode"] = "ref_cap"
        meta["effective_max"] = ref
        return ref, meta
    if legacy_max is not None and legacy_max > 0:
        meta["mode"] = "legacy"
        meta["effective_max"] = legacy_max
        return legacy_max, meta
    return None, meta


