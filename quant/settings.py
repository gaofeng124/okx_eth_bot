def _eb(k: str, default: bool) -> bool:
    raw = os.environ.get(k)
    if raw is None or not raw.strip():
        return default
    v = raw.strip().lower()
    if v in ("1", "true", "yes"):
        return True
    if v in ("0", "false", "no"):
        return False
    return default


def _ei(k: str, default: int) -> int:
    raw = os.environ.get(k, "").strip()
    if not raw:
        return default
    return int(raw)


def _es(k: str, default: str) -> str:
    return os.environ.get(k, default).strip()


def _ef(k: str, default: float) -> float:
    raw = os.environ.get(k, "").strip()
    if not raw:
        return default
    try:
        return float(raw)
    except ValueError:
        return default


